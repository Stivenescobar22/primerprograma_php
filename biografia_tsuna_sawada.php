     <h1>Biografia de sawada Tsunayoshi </h1>
     <h2>
            Es estudiante de Secundaria y Jefe en formación de la familia Vongola. La razón por la cual Tsuna fue elegido es porque él es el tatara-tatara-tataranieto del primer jefe Vongola,
            Giotto, quien se había ido a Japón para retirarse. Asimismo, los demás candidatos, por la posición de la cabeza de la Familia Vongola, fallecieron.

            Antes de que Reborn encontrara a Tsuna, Tsuna era conocido como "Tsuna Inútil" o "Perdedor Tsuna" (Dame-Tsuna) por sus bajas calificaciones, actitud miedosa, mala suerte,
             y la falta de atletismo. Cuando Reborn le dispara con la Bala de la Última Voluntad de la familia Vongola, regresa como un poderoso guerrero medio desnudo (solo tiene su ropa interior puesta)
             con una llama en su frente y omitiese ojos. Sin embargo, esto solo funciona si tiene algún tipo de arrepentimiento en el momento que Reborn le dispara . Después de cinco minutos,
              se convierte de nuevo en su yo normal, por lo general avergonzado por el hecho de que él este en público solo con su ropa interior.   
     </h2>

